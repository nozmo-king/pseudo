function sha256Sync(str) {
    const encoder = new TextEncoder();
    const data = encoder.encode(str);

    function rightRotate(value, amount) {
        return (value >>> amount) | (value << (32 - amount));
    }

    const k = [
        0x428a2f98, 0x71374491, 0xb5c0fbcf, 0xe9b5dba5, 0x3956c25b, 0x59f111f1, 0x923f82a4, 0xab1c5ed5,
        0xd807aa98, 0x12835b01, 0x243185be, 0x550c7dc3, 0x72be5d74, 0x80deb1fe, 0x9bdc06a7, 0xc19bf174,
        0xe49b69c1, 0xefbe4786, 0x0fc19dc6, 0x240ca1cc, 0x2de92c6f, 0x4a7484aa, 0x5cb0a9dc, 0x76f988da,
        0x983e5152, 0xa831c66d, 0xb00327c8, 0xbf597fc7, 0xc6e00bf3, 0xd5a79147, 0x06ca6351, 0x14292967,
        0x27b70a85, 0x2e1b2138, 0x4d2c6dfc, 0x53380d13, 0x650a7354, 0x766a0abb, 0x81c2c92e, 0x92722c85,
        0xa2bfe8a1, 0xa81a664b, 0xc24b8b70, 0xc76c51a3, 0xd192e819, 0xd6990624, 0xf40e3585, 0x106aa070,
        0x19a4c116, 0x1e376c08, 0x2748774c, 0x34b0bcb5, 0x391c0cb3, 0x4ed8aa4a, 0x5b9cca4f, 0x682e6ff3,
        0x748f82ee, 0x78a5636f, 0x84c87814, 0x8cc70208, 0x90befffa, 0xa4506ceb, 0xbef9a3f7, 0xc67178f2
    ];

    let h0 = 0x6a09e667, h1 = 0xbb67ae85, h2 = 0x3c6ef372, h3 = 0xa54ff53a,
        h4 = 0x510e527f, h5 = 0x9b05688c, h6 = 0x1f83d9ab, h7 = 0x5be0cd19;

    const ml = data.length * 8;
    const paddedLength = Math.ceil((data.length + 9) / 64) * 64;
    const padded = new Uint8Array(paddedLength);
    padded.set(data);
    padded[data.length] = 0x80;

    const view = new DataView(padded.buffer);
    view.setUint32(paddedLength - 4, ml, false);

    for (let chunk = 0; chunk < paddedLength; chunk += 64) {
        const w = new Uint32Array(64);
        for (let i = 0; i < 16; i++) {
            w[i] = view.getUint32(chunk + i * 4, false);
        }
        for (let i = 16; i < 64; i++) {
            const s0 = rightRotate(w[i-15], 7) ^ rightRotate(w[i-15], 18) ^ (w[i-15] >>> 3);
            const s1 = rightRotate(w[i-2], 17) ^ rightRotate(w[i-2], 19) ^ (w[i-2] >>> 10);
            w[i] = (w[i-16] + s0 + w[i-7] + s1) | 0;
        }

        let a = h0, b = h1, c = h2, d = h3, e = h4, f = h5, g = h6, h = h7;

        for (let i = 0; i < 64; i++) {
            const S1 = rightRotate(e, 6) ^ rightRotate(e, 11) ^ rightRotate(e, 25);
            const ch = (e & f) ^ (~e & g);
            const temp1 = (h + S1 + ch + k[i] + w[i]) | 0;
            const S0 = rightRotate(a, 2) ^ rightRotate(a, 13) ^ rightRotate(a, 22);
            const maj = (a & b) ^ (a & c) ^ (b & c);
            const temp2 = (S0 + maj) | 0;

            h = g; g = f; f = e; e = (d + temp1) | 0;
            d = c; c = b; b = a; a = (temp1 + temp2) | 0;
        }

        h0 = (h0 + a) | 0; h1 = (h1 + b) | 0; h2 = (h2 + c) | 0; h3 = (h3 + d) | 0;
        h4 = (h4 + e) | 0; h5 = (h5 + f) | 0; h6 = (h6 + g) | 0; h7 = (h7 + h) | 0;
    }

    return [h0, h1, h2, h3, h4, h5, h6, h7]
        .map(v => (v >>> 0).toString(16).padStart(8, '0'))
        .join('');
}

class ProofOfWorkMiner {
    constructor() {
        this.mining = false;
        this.challenge = null;
        this.bestHash = null;
        this.bestNonce = null;
        this.bestPoints = 0;
        this.attempts = 0;
        this.onUpdate = null;
    }

    async getChallenge() {
        const response = await fetch('/api/pow/challenge');
        const data = await response.json();
        this.challenge = data.challenge;
        return data;
    }

    calculatePoints(hash) {
        const prefix = '21e8';
        if (!hash.startsWith(prefix)) {
            return 0;
        }
        let matchLength = prefix.length;
        for (let i = prefix.length; i < hash.length; i++) {
            if (hash[i] === '0') {
                matchLength++;
            } else {
                break;
            }
        }
        const extraZeros = matchLength - prefix.length;
        return 15 * Math.pow(4, extraZeros);
    }

    async mine(targetPoints = 15, onProgress = null) {
        if (!this.challenge) {
            await this.getChallenge();
        }

        this.mining = true;
        this.bestHash = null;
        this.bestNonce = null;
        this.bestPoints = 0;
        this.attempts = 0;

        return new Promise((resolve) => {
            const mineStep = () => {
                if (!this.mining) {
                    resolve({ hash: this.bestHash, nonce: this.bestNonce, points: this.bestPoints });
                    return;
                }

                for (let i = 0; i < 10000; i++) {
                    const nonce = this.randomNonce();
                    const hash = sha256Sync(this.challenge + nonce);
                    const points = this.calculatePoints(hash);

                    this.attempts++;

                    if (points > this.bestPoints) {
                        this.bestPoints = points;
                        this.bestHash = hash;
                        this.bestNonce = nonce;

                        if (onProgress) {
                            onProgress(this.bestHash, this.bestNonce, this.bestPoints, this.attempts);
                        }

                        if (points >= targetPoints) {
                            this.mining = false;
                            resolve({ hash: this.bestHash, nonce: this.bestNonce, points: this.bestPoints });
                            return;
                        }
                    }
                }

                setTimeout(mineStep, 0);
            };

            mineStep();
        });
    }

    startMouseoverMining(element, onUpdate) {
        this.onUpdate = onUpdate;
        let miningActive = false;

        element.addEventListener('mouseenter', async () => {
            if (miningActive) return;
            miningActive = true;
            element.classList.add('mining-active');

            if (!this.challenge) {
                await this.getChallenge();
            }

            this.mining = true;
            this.attempts = 0;

            const mineLoop = () => {
                if (!this.mining || !miningActive) return;

                for (let i = 0; i < 5000; i++) {
                    const nonce = this.randomNonce();
                    const hash = sha256Sync(this.challenge + nonce);
                    const points = this.calculatePoints(hash);

                    this.attempts++;

                    if (points > this.bestPoints) {
                        this.bestPoints = points;
                        this.bestHash = hash;
                        this.bestNonce = nonce;

                        if (this.onUpdate) {
                            this.onUpdate(this.bestHash, this.bestNonce, this.bestPoints, this.attempts);
                        }
                    }
                }

                if (window.updateMiningStats) {
                    window.updateMiningStats(this.bestPoints, this.attempts);
                }

                setTimeout(mineLoop, 0);
            };

            mineLoop();
        });

        element.addEventListener('mouseleave', () => {
            this.mining = false;
            miningActive = false;
            element.classList.remove('mining-active');
        });
    }

    stopMining() {
        this.mining = false;
    }

    randomNonce() {
        return Array.from(crypto.getRandomValues(new Uint8Array(32)))
            .map(b => b.toString(16).padStart(2, '0'))
            .join('');
    }

    async hash(data) {
        const encoder = new TextEncoder();
        const dataBuffer = encoder.encode(data);
        const hashBuffer = await crypto.subtle.digest('SHA-256', dataBuffer);
        return this.bufferToHex(hashBuffer);
    }

    bufferToHex(buffer) {
        const bytes = new Uint8Array(buffer);
        return Array.from(bytes)
            .map(b => b.toString(16).padStart(2, '0'))
            .join('');
    }
}

const powMiner = new ProofOfWorkMiner();
